export class MathFunctions {
  public static add(a: number, b: number) {
    return a + b;
  }

  public static minus(a: number, b: number) {
    return a - b;
  }

  public static multiply(a: number, b: number) {
    return a * b;
  }

  public static divide(a: number, b: number) {
    return a / b;
  }

  public static gt(a: number, b: number) {
    return a > b;
  }

  public static ge(a: number, b: number) {
    return a >= b;
  }

  public static lt(a: number, b: number) {
    return a < b;
  }

  public static le(a: number, b: number) {
    return a <= b;
  }
}